void TryRunTics (void)
{
	int i;
	int lowtic;
	int entertic;

	entertic = I_GetTime ();

	do
	{
		NetUpdate ();
		GetPackets ();

		lowtic = MAXINT;
		for (i=0 ; i<numnetnodes ; i++)
			if (nodeingame[i] && nettics[i] < lowtic)
				lowtic = nettics[i];

		if (lowtic < gametic)
			I_Error("TryRunTics: lowtic < gametic");

		// don't stay in here forever -- give the menu a chance to work
		if (I_GetTime () - entertic >= 20)
		{
			M_Ticker ();
			return;
		}

	} while (lowtic == gametic);

	if (netgame && lowtic == maketic && !drone)
	{
		int_632C8++;
		if (int_632C8 > 4)
		{
			int_632C8 = 0;
			gametime--;
		}
	}
	else
		int_632C8 = 0;

	do
	{
		if (advancedemo)
			D_DoAdvanceDemo ();
		M_Ticker ();
		G_Ticker ();
		gametic++;
		for (i = 0; i < numnetnodes; i++)
			if (nodeingame[i])
			{
				if (nettics[i] < gametic)
					I_Error("Run an improper tic");
				if (nettics[i] == gametic)
					break;
			}
	} while (i == numnetnodes);
}

/*
=============
=
= NetUpdate
=
= Builds ticcmds for console player
= sends out a packet
=============
*/

void NetUpdate (void)
{
	int             nowtime;
	int             newtics;

	if (singletics)
		return;         // singletic update is syncronous
	
//
// drone packets
//
	if (drone)
	{
		I_StartTic ();
		D_ProcessEvents( );

		netbuffer.player = (drone << 7) | (consoleplayer << 5) | netnode;
		netbuffer.tic = gametic + 8;
		SendPacket ();
		return;
	}

//
// check time
//
	nowtime = I_GetTime ();
	newtics = nowtime - gametime;
	if (newtics <= 0)                       // nothing new to update
		return;

	gametime = nowtime;

	while (newtics-- > 0)
	{
		I_StartTic ();
		D_ProcessEvents ();

		if (maketic - gametic >= 8)
			break;
		G_BuildTiccmd (&localcmds[maketic & (BACKUPTICS-1)]);
		maketic++;
	}

//
// send the packet to the other nodes
//
	netbuffer.player = (drone << 7) | (consoleplayer << 5) | netnode;
	netbuffer.tic = maketic;
	memcpy(netbuffer.cmds, localcmds, sizeof(localcmds));
	SendPacket ();
}


